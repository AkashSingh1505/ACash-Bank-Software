    private void jLabel31MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel31MouseClicked
        try {
            JFileChooser fc = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG/PNG File", "jpg","jpeg","png");
            fc.addChoosableFileFilter(filter);
            fc.showOpenDialog(null);
            File fs = fc.getSelectedFile();
            
            if(fs==null){
                JOptionPane.showMessageDialog(null, "File is not found !", "Error", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-error-16.png")));
            }
            else{
                String extension = fs.getPath().toString().toLowerCase();
                if(extension.endsWith(".jpg")||extension.endsWith(".png")){
                    UserProfileData.imageExtension = extension(fs.getPath().toString().toLowerCase());
                   
                   ImageIcon imageicon = new ImageIcon(fs.getAbsolutePath());
                   long size = fs.length();
                    if(size<16777216){
                    isImageUploaded=true;
                    java.awt.Image image = imageicon.getImage();
                    java.awt.Image scaledImage = image.getScaledInstance(jLabel31.getWidth(), jLabel31.getHeight(), java.awt.Image.SCALE_AREA_AVERAGING);
                    jLabel31.setIcon(new ImageIcon(scaledImage));
                    userProfileData.UserImage = new FileInputStream(fs);//this is used when we save it in database;
                    }
                     else{
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(null, "Size must be smaller then 16MB", "Error", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-error-16.png")));
                    }
                }
                else{
                    Toolkit.getDefaultToolkit().beep();
                     JOptionPane.showMessageDialog(null, "File formate must be JPG/JPEG/PNG !", "Error", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-error-16.png")));
                }
              
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLabel31MouseClicked
    private String extension(String ApsolutePath){
        StringBuilder sb = new StringBuilder(ApsolutePath);
        sb=sb.reverse();
        String s = sb.toString();
        StringBuilder ss = new StringBuilder(s.substring(0, 3));
        return ss.reverse().toString();
    }
