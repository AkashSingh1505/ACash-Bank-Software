    private void submitBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtActionPerformed
        

//CustomeType
        if(publicBt.isSelected()==false && adminBt.isSelected()==false){
          
           Warning.warning(publicBt, false);
        }else{
            Warning.removeWarning(publicBt,"lightBlack");
            userProfileData.CustomerType=(publicBt.isSelected()==true)?"Public":"Admin";
        }
       
//AccountType        
        if(isSelected(jComboBox11)){
            Warning.removeWarning(jComboBox11,"black");
            userProfileData.AccountType= jComboBox11.getSelectedItem().toString();
        }
        else{Warning.warning(jComboBox11, false);
        }
        
//Religion
        if(isSelected(jComboBox9)){
            Warning.removeWarning(jComboBox9,"black");
            userProfileData.Religion= jComboBox9.getSelectedItem().toString();
        }
        else{Warning.warning(jComboBox9, false);
        }
        
// City        
        if(isSelected(jComboBox8)){
            Warning.removeWarning(jComboBox8,"black");
            userProfileData.City = jComboBox8.getSelectedItem().toString();
        }
        else{Warning.warning(jComboBox8, false);}
        
// Pin  
        if(!jTextField8.getText().isBlank()){
            Warning.removeWarning(jTextField8,"black"); 
            userProfileData.PinCode=jTextField8.getText();
        }else{Warning.warning(jTextField8, false);}
        
// State        
        if(isSelected(jComboBox7)&&jComboBox7.getForeground().equals(Color.white)){
            Warning.removeWarning(jComboBox7,"black");
            userProfileData.State = jComboBox7.getSelectedItem().toString();
        }
        else{Warning.warning(jComboBox7, false);}        

//Date of birth        
        if(isSelected(jComboBox3)){
            Warning.removeWarning(jComboBox3,"black");
            if(isSelected(jComboBox4)){
                Warning.removeWarning(jComboBox4,"black");
                if(isSelected(jComboBox5)){
                    Warning.removeWarning(jComboBox5,"black");
                    userProfileData.DateOfBirth=jComboBox3.getSelectedItem()+"/"+jComboBox4.getSelectedItem()+"/"+jComboBox5.getSelectedItem();
                }else{Warning.warning(jComboBox5, false);}
            }else{Warning.warning(jComboBox4, false);}
        }else{Warning.warning(jComboBox3, false);}
        
//Fathers/mother name
      if(!jTextField3.getText().isBlank()&&jTextField3.getForeground()==Color.white){
                 Warning.removeWarning(jTextField3,"black");
                 userProfileData.ParentsName=jComboBox2.getSelectedItem()+" "+jTextField3.getText();
             }else{Warning.warning(jTextField3, false);}
        
//Customer Name
         if(!jTextField1.getText().isBlank()&&jTextField1.getForeground()==Color.white){
             Warning.removeWarning(jTextField1,"black");
             if(!jTextField2.getText().isBlank()&&jTextField2.getForeground()==Color.white){
                 Warning.removeWarning(jTextField2,"black");
                 userProfileData.CustomerName=jComboBox1.getSelectedItem()+" "+jTextField1.getText()+" "+jTextField2.getText();
             }else{Warning.warning(jTextField2, false);}
         }else{Warning.warning(jTextField1, false);}
         
//CustomerUId NUmber
        if(CustomerUID.isEditValid()){
            Warning.removeWarning(CustomerUID,"black");
        }else{Warning.warning(CustomerUID, false);}
// phn number
        if(!jTextField7.getText().isBlank()){
            Warning.removeWarning(jTextField7,"black"); 
            userProfileData.MobileNumber=jTextField7.getText();
        }else{Warning.warning(jTextField7, false);}
        
//Gender
        if(jRadioButton3.isSelected()==false && jRadioButton4.isSelected()==false){
           Warning.warning(jLabel17, false);
        }else{
            Warning.removeWarning(jLabel17,"lightBlack");
            userProfileData.Gender=(jRadioButton3.isSelected()==true)?"Male":"Female";
        }
        
//Material Status
        if(isSelected(jComboBox10)){
            Warning.removeWarning(jComboBox10,"black");
            userProfileData.MaterialStatus=jComboBox10.getSelectedItem().toString();
        }else{Warning.warning(jComboBox10, false);}
        

//Nationallity
        if(isSelected(jComboBox6)){
            Warning.removeWarning(jComboBox6,"black");
            userProfileData.Nationality=jComboBox6.getSelectedItem().toString();
        }else{Warning.warning(jComboBox6, false);}
        
//NOmini
        if(nomini_checkBox.isSelected()){
            if(!nomini_name.getText().isBlank()){
                 Warning.removeWarning(nomini_name,"black ");
                if(isSelected(nomini_relation)==true){
                     Warning.removeWarning(nomini_relation,"black");
                    if(nomini_uid.isEditValid()){
                          Warning.removeWarning(nomini_uid,"black");
                         userProfileData.Nomini=nomini_checkBox.isSelected();
                         userProfileData.NominiName=nomini_name.getText();
                         userProfileData.NominiRelation=nomini_relation.getSelectedItem().toString();
                         userProfileData.NominiUID = nomini_uid.getText();
                    }else{Warning.warning(nomini_uid, false);}
                }else{Warning.warning(nomini_relation, false);}
            }else{Warning.warning(nomini_name, false);}
        }
        System.out.println(nomini_uid.getText().length());
//address
        if(!jTextField4.getText().isBlank()){
            Warning.removeWarning(jTextField4,"black");
            userProfileData.PermanentAddress=jTextField4.getText();
        }else{Warning.warning(jTextField4, false);}
        
        userProfileData.ChequeBook=jCheckBox4.isSelected();
        userProfileData.CreaditCard = jCheckBox2.isSelected();
        userProfileData.DebitCard=jCheckBox3.isSelected();
        userProfileData.EmailAddress=jTextField5.getText();
        
        userProfileData.UID=CustomerUID.getText();
        
//Image
       if(!isImageUploaded){
           jLabel31.setBorder(new LineBorder(Color.red));
       }
       if(!isWarningVisible()){
           //save in data base
           
           java.awt.EventQueue.invokeLater(new Runnable(){
               public void run(){
                   try{
                   new BotDialog(userProfileData,openedForm).setVisible(true);
                   }catch(Exception e){
                       e.printStackTrace();
                   }
               }
           });
           
       }else{
           Toolkit.getDefaultToolkit().beep();
       }
       
    }//GEN-LAST:event_submitBtActionPerformed
   
    private boolean isSelected(JComboBox t){
        return (t.getSelectedIndex()>0);
    }
    private boolean isWarn(JComponent j){
        return (((LineBorder)j.getBorder()).getLineColor()==Color.red);
    }
    private boolean isWarningVisible(){
        if(isWarn(publicBt)||isWarn(jComboBox8)||isWarn(jComboBox7)||isWarn(jComboBox6)||isWarn(jComboBox10)||isWarn(jComboBox3)
                ||isWarn(jComboBox4)||isWarn(jComboBox5)||isWarn(jTextField1)||isWarn(jTextField2)||isWarn(jTextField3)||isWarn(jTextField4)||isWarn(jComboBox9)
                ||isWarn(CustomerUID)||isWarn(jTextField7)||isWarn(jTextField8)||isWarn(jLabel17)
                ||!isImageUploaded){
                        return true;}
            if(jPanel4.isVisible()){
                return isWarn(jComboBox11);
            }
            
            if(nomini_checkBox.isSelected()){
                return (isWarn(nomini_name)||isWarn(nomini_relation)||isWarn(nomini_uid));
            }
            return false;
    }
