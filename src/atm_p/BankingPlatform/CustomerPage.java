    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
      try{ 
          Statement stmt = con.createStatement();
          ResultSet rs = stmt.executeQuery("select CID from app.otherrqst where CID ="+"'"+customerRS.getString(1)+"'");
          if(!rs.next()){
                PreparedStatement pstmt1 = con.prepareStatement("insert into app.otherrqst (CID) values (?)");
                PreparedStatement pstmt2 = con.prepareStatement("insert into app.otherrqstData (CID) values (?)");
                pstmt1.setString(1, customerRS.getString(1));
                pstmt2.setString(1, customerRS.getString(1));
                pstmt1.executeUpdate();
                pstmt2.executeUpdate();
          }
          else{
              Statement stmt1 = con.createStatement();
               ResultSet rs1 = stmt.executeQuery("select * from app.otherrqst where CID ="+"'"+customerRS.getString(1)+"'");
               if(rs1.next()){
                   if(rs1.getBoolean(2)){preRqst(jButton20,jRadioButton2);}
                   if(rs1.getBoolean(3)){preRqst(jButton21,jRadioButton3);}
                   if(rs1.getBoolean(4)){preRqst(jButton22,jRadioButton4);}
                   if(rs1.getBoolean(5)){preRqst(jButton23,jRadioButton6);}
                   if(rs1.getBoolean(6)){preRqst(jButton24,jRadioButton7);}
                   if(rs1.getBoolean(7)){preRqst(jButton25,jRadioButton5);}
                   if(rs1.getBoolean(8)){preRqst(jButton26,jRadioButton8);}
                   if(rs1.getBoolean(9)){preRqst(jButton27,jRadioButton9);}
               }
          }
      }catch(Exception e){
          e.printStackTrace();
        JOptionPane.showMessageDialog(null, "unable to create request base","Error",JOptionPane.INFORMATION_MESSAGE,new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-error.png")));
 
      }
    }//GEN-LAST:event_jButton12ActionPerformed

