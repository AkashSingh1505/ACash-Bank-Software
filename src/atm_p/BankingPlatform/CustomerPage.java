    private void setRequest(String columnName,boolean value){
        try{
        PreparedStatement pstmt1 = con.prepareStatement("update  app.Otherrqst set "+columnName+" = "+value+" where CID = ?");
        PreparedStatement pstmt2 = con.prepareStatement("update  app.Otherrqst set request = true where CID = ?");
        pstmt2.setString(1, customerRS.getString(1));
        pstmt1.setString(1, customerRS.getString(1));
        pstmt1.executeUpdate();
        pstmt2.executeUpdate();
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "unable to set Request !","Error",JOptionPane.INFORMATION_MESSAGE,new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-error.png")));

        }
    }
