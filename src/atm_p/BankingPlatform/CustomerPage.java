    private void done(JProgressBar bar){
            new Thread(new Runnable (){
                public void run(){
                    for(int i =10;i<=100;i+=10){
                        doTask(i,bar);
                    }
                    if(bar.equals(jProgressBar1)){
                    jLabel94.setIcon(new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-done-35 (1).png")));
                    jLabel97.setForeground(new Color(52,149,215));
                    Step2.setVisible(true);}
                    if(bar.equals(jProgressBar2)){
                        Sound.play("PaySuccessful");
                        jLabel96.setIcon(new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-done-35 (1).png")));
                        jLabel98.setForeground(new Color(52,149,215));
                        Step3.setVisible(true);
                    }

                }
            }).start();
    }
