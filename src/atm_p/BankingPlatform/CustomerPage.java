    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
       if(isSelected(jComboBox2)){
           inerActivity1.setVisible(true);
            if(jComboBox2.getSelectedIndex()==1){
                debitInerCard.show(inerActivity1, "card1");
                int i=setImage(QRL, "DQrcode");
                if(i==0){
                    CARD.setVisible(false);
                    warn2.setVisible(true);
                }
            }
            else if(jComboBox2.getSelectedIndex()==5){
                debitInerCard.show(inerActivity1, "card3");
            }
            else{
                int n=jComboBox2.getSelectedIndex();
                 debitInerCard.show(inerActivity1, "card2");
                 if(n==2){
                     jComboBox6.setVisible(false);
                     jFormattedTextField10.setVisible(false);
                     jLabel106.setText("Click on submit Button to send Your Application to the Admin for new Debit Card");
                 }
                 else if(n==3){
                     jComboBox6.setVisible(false);
                     jLabel106.setText("Verify your PIN to Deactivate the Card");
                     jFormattedTextField10.setVisible(true);
                 }
                 else if(n==4){
                     jComboBox6.setVisible(false);
                     jLabel106.setText("Verify your PIN to Block the Card permanently");
                     jFormattedTextField10.setVisible(true);
                 }
                 else if(n==6){
                     jLabel106.setText("Select Card Limit");
                     jComboBox6.setVisible(true);
                     jFormattedTextField10.setVisible(true);
                 }
                 
            }
        }
        else{
            inerActivity1.setVisible(false);
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

