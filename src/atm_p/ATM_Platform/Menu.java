    private ResultSet getResultSet(Connection con,String table,String CID){
        ResultSet rs = null;
        try{
            Statement stmt1 = con.createStatement();
            rs= stmt1.executeQuery("select * from app."+table+" where CID = '"+CID+"'");
        }
        catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Unable to fetch details !","Error",JOptionPane.INFORMATION_MESSAGE,new ImageIcon(getClass().getResource("/atm_p/pictures/icons8-error.png")));
            dispose();
        }
        return rs;
    }
