        if(pinSpeech.isRunning()){pinSpeech.stop();}
        Sound.play("click");
        ResultSet debit = getResultSet(con, "DEBITCARDDETAILS", CID);
        String entPin = String.valueOf(jPasswordField4.getPassword());
        try{
            if(debit.next()){
                String orgPin = debit.getString(6);
                if(orgPin.equals(entPin)){
                    wrongCount=4;//it will be usefull for continus money debiting may be
                    MenuCard.show(Blank, "card3");
                    chooseBanking=Sound.play("bankingChooseSpeech");
                }
                else{
                    wrongCount--;
                    Toolkit.getDefaultToolkit().beep();
                    if(wrongCount>0){
                        MszDialog.showDialog("info","Incorrect PIN "+wrongCount+" attempts remaining");
                        jPasswordField4.setText("");
                    }
                    else{
                        //block the card
                        if(setBlocked()!=0){
                            Toolkit.getDefaultToolkit().beep();
                            MszDialog.showDialog("warn","Your card has been blocked");
                            dispose();
                        }
                        else{
                            Toolkit.getDefaultToolkit().beep();
                            MszDialog.showDialog("info", "System failure (Unable to complete blocking process)");
                            dispose();
                        }
                    }
                }
            }
            else{
                Toolkit.getDefaultToolkit().beep();
                MszDialog.showDialog("info", "No PIN detected first set new PIN");
                Sound.play("newPinSpeech");
                MenuCard.show(Blank, "card10");
            }
        }
        catch(Exception e){
            Toolkit.getDefaultToolkit().beep();
            MszDialog.showDialog("warn", "System failure (Unable to catch the User PIN From Bank)");
            dispose();
        }
        
        
